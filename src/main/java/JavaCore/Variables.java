package JavaCore;

public class Variables {

    public static void main(String[] args) {

        byte byteNumber = 123;
        short shortNumber = 7;

        int intNumber = 7777777;
        long longNumber = 12345678910111111L;

        float floatNumber = 777.7f;
        double doubleNumber = 777.77777777777777777;

        char charSymbol = '@';
        boolean booleanValue = true;

        String string = "Hello";

        System.out.println("My intNumber is: " + intNumber);
        System.out.println("My longNumber is: " + longNumber);
        System.out.println("My byteNumber is: " + byteNumber);
        System.out.println("My shortNumber is: " + shortNumber);
        System.out.println("My floatNumber is: " + floatNumber);
        System.out.println("My doubleNumber is: " + doubleNumber);
        System.out.println("My charSymbol is: " + charSymbol);
        System.out.println("My booleanValue is: " + booleanValue);
        System.out.println("My string is: " + string);


    }
}
